package com.test;

public class UnaryOpreator {

	public static void main(String[] args) {
	
		int no=10;
		//post opration
		System.out.println(no++);
		System.out.println(no);
		//pre opration
		System.out.println(++no);
		System.out.println(no);
		//post decrement opration
		System.out.println(no--);
		System.out.println(no);
		//pre decrement opration
		System.out.println(--no);
		System.out.println(no);
		}

}
